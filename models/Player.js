class Player {
  constructor(id, name, team, games, goals, assists, image, nr, position) {
    this.id = id;
    this.name = name;
    this.team = team;
    this.games = games
    this.goals = goals;
    this.assists = assists
    this.image = image;
    this.nr = nr;
    this.position = position
  }
}

export default Player;
